package Module1;

public abstract class forLoop {
	
	public  void myname() {
	}

	public static void main(String[] args) {
		
//		1.while
//		2. do while
//		3. for loop
		
//		print 1 to 100 numbers 
		
		int a = 1;
		while(a<=100) {
			System.out.println(a);
			a++;
		}
		System.out.println("==================");
		int b = 1;
		do {
			System.out.println(b);
			b++;
		}
		while(b<=100);
		System.out.println("==================");
		for(int c=1; c <=100;c++) {
			System.out.println(c);
		}
		
//		

		
		
		

	}

}
